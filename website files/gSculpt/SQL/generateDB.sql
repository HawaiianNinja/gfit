CREATE PROCEDURE [dbo].[usp_getAccountByFacebookUid]
	@FacebookUid nvarchar(MAX) = 0
AS
	SELECT 
		A.id, 
		A.username,
		A.firstName, 
		A.lastName, 
		A.dob, 
		A.gender, 
		A.fbUserId, 
		A.fbAuthToken,
		A.dateCreated,
		A.dateCreated
	FROM ACCOUNTS A
	WHERE A.fbUserId = @FacebookUid
RETURN 0
GO
