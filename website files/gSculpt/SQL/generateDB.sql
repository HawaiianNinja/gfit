CREATE PROCEDURE [dbo].[usp_addAccount]
	@username nvarchar(32) = null,
	@firstname nvarchar(64) = null,
	@lastname nvarchar(64) = null,
	@dob date = null,
	@gender nvarchar(16) = null,
	@fbUserId nvarchar(MAX) = null,
	@fbAuthToken nvarchar(MAX) = null

AS

IF(@username is null)
BEGIN
	PRINT '@username is NULL. @username cannot be NULL'
	RETURN -1
END

ELSE IF(@fbUserId IS NULL)
BEGIN
	PRINT '@fbUserId is NULL. @fbUserId cannot be NULL'
	RETURN -2
END
ELSE
BEGIN
	
	IF(@@ERROR <> 0)
	BEGIN
		RETURN @@ERROR
	END

	INSERT INTO ACCOUNTS
	(username, firstName, lastName, dob, gender, fbUserId, fbAuthToken)
	VALUES 
	(@username, @firstName, @lastName, @dob, @gender, @fbUserId, @fbAuthToken)


	RETURN @@ERROR


END



RETURN 0
GO
